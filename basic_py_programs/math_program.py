# asks user to input two numbers and then adds, subs, mults, divs those two numbers and outputs them

num1 = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

add = num1 + num2 
sub = num1 - num2
mult = num1 * num2
div = num1 / num2

print(f"Both numbers added equals {add}, both numbers subtracted equals {sub}, multiplied equals {mult} and divided equals {div}")
